// Preprocessor directives
#include <stdio.h>
#include <stdlib.h>



int main()
{
    int a = 20;
    int b = 3;
    printf("a + b = %d\n", (a + b));
    printf("a - b = %d\n", (a - b));
    printf("a * b = %d\n", (a * b));
    printf("a / b = %d\n", (a / b ));
    printf("a mod b = %d\n", (a % b));

    return 0;

}